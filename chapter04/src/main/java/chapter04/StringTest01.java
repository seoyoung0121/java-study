package chapter04;

public class StringTest01 {
	public static void main(String[] args) {
		// c:\temp
		System.out.println("c:\temp"); // c:	emp
		System.out.println("c:\\temp"); // c:\temp
		
		// \t: tab
		// \r: carriage return
		// \n: new line
		// \b: beep 스피커가 삐하고 울림 
		
		// "hello" 
		System.out.println("\"hello\"");
		
		//'
		char c= '\'';
		System.out.println(c);
		
	}
}
